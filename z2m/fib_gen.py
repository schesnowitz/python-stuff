def fibbi_yield(num_of_loops):
    a = 0
    b = 1
    for x in range(num_of_loops):
        yield a # 0
        temp = a # 0
        a = b # 1
        b = temp + b # 0 + 1


for x in fibbi_yield(100):
    print(x)

