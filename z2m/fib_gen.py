def fibbi_list(num_of_loops):
    a = 0
    b = 1
    result = []
    for x in range(num_of_loops):
        result.append(a)
        temp = a # 0
        a = b # 1
        b = temp + b # 0 + 1
    return result


fibbi_list(100)